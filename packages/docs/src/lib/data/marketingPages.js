export const marketingPages = marketingClusters.flatMap((cluster) =>
  cluster.pages.map((page) => ({
    ...page,
    cluster: cluster.slug,
    path: page.slug ? `/${cluster.slug}/${page.slug}/` : `/${cluster.slug}/`,
  })),
)

export const marketingLegacyRedirects = Object.fromEntries(
  marketingPages.flatMap((page) => page.legacySlugs.map((legacySlug) => [legacySlug, page.path])),
)

const marketingPagesByPath = new Map(marketingPages.map((page) => [page.path, page]))

export function getMarketingCluster(pathname) {
  const clusterSlug = pathname.split("/").filter(Boolean)[0]
  return marketingClusters.find((cluster) => cluster.slug === clusterSlug) || null
}

export function getLegacyMarketingImage(pathname) {
  const normalizedPath = pathname.endsWith("/") ? pathname : `${pathname}/`
  const page = marketingPagesByPath.get(normalizedPath)
  const legacySlug = page?.legacySlugs[0]

  return legacySlug ? `https://img.daisyui.com/images/pages/${legacySlug}.webp` : null
}
